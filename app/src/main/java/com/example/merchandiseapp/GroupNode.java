package com.example.merchandiseapp;

public class GroupNode {

    public String GroupName;
    public String Contact;
    public String EmailID;
    public String UPI;

    public GroupNode(String groupName, String contact, String emailID, String UPI) {
        GroupName = groupName;
        Contact = contact;
        EmailID = emailID;
        this.UPI = UPI;
    }
}

